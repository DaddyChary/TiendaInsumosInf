    private void tbl_productoMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_productoMouseReleased
        try {
            int dato = tbl_producto.getSelectedRow();
            Object id = tbl_producto.getValueAt(dato, 0);
            Producto producto = manager.getdProducto().getOne(Integer.parseInt(id.toString()));
            txt_id.setText(producto.getIdString());
            txt_nombre.setText(producto.getNombre());
            txt_precio.setText(producto.getPrecioString());
            txt_descripcion.setText(producto.getDescripcion());
        } catch (SQLException ex) {
            Logger.getLogger(ProductosModule.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showConfirmDialog(null, "Error al agregar los campos", "Aceptar", JOptionPane.DEFAULT_OPTION);
        } catch (NumberFormatException e) {
            JOptionPane.showConfirmDialog(null, "Ingrese solo numeros", "Aceptar", JOptionPane.DEFAULT_OPTION);
        }

    }//GEN-LAST:event_tbl_productoMouseReleased

