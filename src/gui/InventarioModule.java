    private void btn_CreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CreateActionPerformed
        try {
            // TODO add your handling code here:
            Inventario inventario = new Inventario();
            Producto producto = new Producto();

            String productos = cbo_Productos_Inventario.getSelectedItem().toString();
            Producto idProducto = manager.getdProducto().getOneByName(productos);
            inventario.setId_producto_fk(idProducto.getId());
            inventario.setId_estanteria_fk(Integer.parseInt(cbo_id_estante.getSelectedItem().toString()));
            inventario.setCantidad(Integer.parseInt(txt_cantidad_Inventario.getText()));
            manager.getdInventario().create(inventario);
            actualizarTablaInventario();
            limpiarCampos();

        } catch (SQLException ex) {
            Logger.getLogger(InventarioModule.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btn_CreateActionPerformed

