    public void actualizarComboboxProductos() throws SQLException {
        Producto producto = new Producto();
        cbo_Productos_Inventario.removeAllItems();
        List<String> nombres;
        nombres = manager.getdProducto().getNameProducto();
        for (String nombre : nombres) {
            cbo_Productos_Inventario.addItem(nombre);
        }
    }

    public void actualizarComboboxIdEstanteria() throws SQLException {
        Estanteria estanteria = new Estanteria();
        cbo_id_estante.removeAllItems();
        List<String> ids;
        ids = manager.getdEstanteria().getIdEstanterias();
        for (String id : ids) {
            cbo_id_estante.addItem(id);
        }
    }

    public void actualizarTablaInventario() throws SQLException {
        DAOInventario dao = manager.getdInventario();
        List<Inventario> lista = dao.getAll();
        TMInventario tmInventario = new TMInventario(lista);
        tbl_Inventario.setModel(tmInventario);
    }

