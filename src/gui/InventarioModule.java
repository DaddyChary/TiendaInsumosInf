    private void tbl_InventarioMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_InventarioMouseReleased
        // TODO add your handling code here:
        try {
            int dato = tbl_Inventario.getSelectedRow();
            Object id = tbl_Inventario.getValueAt(dato, 0);
            Inventario inventario = manager.getdInventario().getOne(Integer.parseInt(id.toString()));
            txt_ID_Inventario.setText(inventario.getIdString());
            txt_cantidad_Inventario.setText(inventario.getCantidadString());
        } catch (SQLException ex) {
            Logger.getLogger(ProductosModule.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showConfirmDialog(null, "Error al agregar los campos", "Aceptar", JOptionPane.DEFAULT_OPTION);
        } catch (NumberFormatException e) {
            JOptionPane.showConfirmDialog(null, "Ingrese solo numeros", "Aceptar", JOptionPane.DEFAULT_OPTION);
        }


    }//GEN-LAST:event_tbl_InventarioMouseReleased

